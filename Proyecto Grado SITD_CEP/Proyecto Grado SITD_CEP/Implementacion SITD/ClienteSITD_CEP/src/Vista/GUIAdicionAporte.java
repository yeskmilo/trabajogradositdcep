    private void validarInicio() {
        if (numeroConvenio != 0 && !NIT.equals("")) {
            lblNumeroConvenioVal.setText(String.valueOf(numeroConvenio));
            lblNitValor.setText(NIT);
        } else {
            ocultarCampos();
        }
    }

    private void ocultarCampos() {
        lblNumeroConvenioVal.setVisible(false);
        lblNitValor.setVisible(false);
    }

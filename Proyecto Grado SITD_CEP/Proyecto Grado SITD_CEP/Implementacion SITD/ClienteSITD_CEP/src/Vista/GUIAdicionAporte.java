private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
// TODO add your handling code here:
    Date fechaDesembolso = null;
    boolean controlDatos = true;
    double montoAporte = 0;
    if (txtMonto.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Debe Digitar un Monto para el aporte", "Error monto aporte", 0);
        controlDatos = false;
    } else {
        montoAporte = Double.parseDouble(txtMonto.getText().trim());
    }
    if (controlDatos) {
        try {
            fechaDesembolso = formatoFecha.parse(comboFechaDesembolso.getText());
            Aportes_convenio aporte = new Aportes_convenio(0, fechaDesembolso, montoAporte, NIT, numeroConvenio);
            boolean controlAdicion = false;
            controlAdicion = controlAporte.AdicionAporte(aporte);
            if (controlAdicion) {
                JOptionPane.showMessageDialog(rootPane, "Registro de aporte exitoso", "Aporte Registrado", 1);
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se pudo registrar el aporte", "No se pudo registrar el aporte", 2);
            }
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }

    }
}//GEN-LAST:event_btnAgregarActionPerformed

private void btn_buscar_entidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscar_entidadActionPerformed
// TODO add your handling code here:
  String NIT = "";
  boolean controlDatos = true;
  if (txt_Nit_Buscar.getText().trim().equals("")) {
    JOptionPane.showMessageDialog(rootPane, "Debe Digitar un NIT para buscar una entidad", "Error en NIT", 0);
    controlDatos = false;
  } else {
    NIT = txt_Nit_Buscar.getText().trim();
  }
  if (controlDatos) {
    try {
      Entidad_convenio entidad = controlEntidad.ConsultaEntidad(NIT);
      if (entidad != null) {
        cargarDatos(entidad);
      } else {
        JOptionPane.showMessageDialog(rootPane, "No se encontraron entidades con NIT suministrado", "No hay resultados", 2);
        deshabilitarCampos();
      }
    } catch (RemoteException ex) {
      Logger.getLogger(GUIBuscarEntidad.class.getName()).log(Level.SEVERE, null, ex);
    }
  }
}//GEN-LAST:event_btn_buscar_entidadActionPerformed
  

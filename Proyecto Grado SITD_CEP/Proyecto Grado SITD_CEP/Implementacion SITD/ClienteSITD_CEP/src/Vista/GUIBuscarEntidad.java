  private void deshabilitarCampos() {
    lblNombreResultado.setVisible(false);
    lblNitResultado.setVisible(false);
    txtDescripcion.setVisible(false);
    btnAgregar_al_convenio.setVisible(false);
  }
  
  private void cargarDatos(Entidad_convenio entidad) {
    habilitarCampos();
    lblNombreResultado.setText(entidad.getNombre_entidad());
    lblNitResultado.setText(entidad.getNIT_entidad());
    txtDescripcion.setText(entidad.getDescripcion_entidad());
  }
  
  private void habilitarCampos() {
    lblNombreResultado.setVisible(true);
    lblNitResultado.setVisible(true);
    txtDescripcion.setVisible(true);
    if (numeroConvenio != 0) {
      btnAgregar_al_convenio.setVisible(true);
    }
  }
  
  private void limpiarFormulario() {
    txt_Nit_Buscar.setText("");
    lblNitResultado.setText("");
    lblNombreResultado.setText("");
    txtDescripcion.setText("");
  }

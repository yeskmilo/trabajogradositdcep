private void btnAgregar_al_convenioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar_al_convenioActionPerformed
// TODO add your handling code here:
  String NIT = lblNitResultado.getText().trim();
  int convenio = numeroConvenio;
  Convenio_entidad convenio_entidad = new Convenio_entidad(0, convenio, NIT);
  boolean controlRelacion = false;
  try {
    controlRelacion = controlConvenio_entidad.AdicionConvenio_entidad(convenio_entidad);
    if (controlRelacion) {
      JOptionPane.showMessageDialog(rootPane, "Se ha añadido la entidad buscada al convenio numero: " + numeroConvenio, "Relacion creada", 1);
      limpiarFormulario();
    } else {
      JOptionPane.showMessageDialog(rootPane, "No se pudo añadir la entidad buscada al convenio: " + numeroConvenio, "Error creando la relacion", 2);
    }
  } catch (Exception e) {
    System.out.println(e.getMessage());
  }
}//GEN-LAST:event_btnAgregar_al_convenioActionPerformed
